async function generateLessonPlanPDFWithHtml(
  lessonPlan: LessonPlanData,
  topic: string
): Promise<Buffer> {
  const html = renderLessonPlanPdfHtml(lessonPlan, topic);
  const playwrightModule = await import("playwright").catch(() => null as any);
  if (!playwrightModule?.chromium) {
    throw new Error("Playwright is not available for HTML PDF generation.");
  }
  const browser = await playwrightModule.chromium.launch({
    headless: true,
    args: ["--no-sandbox", "--disable-setuid-sandbox"],
  });
  try {
    const page = await browser.newPage();
    await page.setContent(html, { waitUntil: "networkidle" });
    const pdf = await page.pdf({
      format: "A4",
      printBackground: true,
      margin: {
        top: "12mm",
        right: "10mm",
        bottom: "12mm",
        left: "10mm",
      },
    });
    return Buffer.from(pdf);
  } finally {
    await browser.close();
  }
}

async function generateLessonPlanPDFWithPdfKit(
  lessonPlan: LessonPlanData,
  topic: string
): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    try {
      const framework = getLessonPlanFramework(lessonPlan?.framework);
      const doc = new PDFDocument({
        margin: MARGIN,
        size: "A4",
        bufferPages: true,
        info: {
          Title: `Lesson Plan: ${sanitize(lessonPlan?.title) || "Lesson Plan"}`,
          Author: "QuizMintAI",
          Subject: "Lesson Plan",
          Creator: "QuizMintAI",
          Producer: "QuizMintAI PDF Export",
          CreationDate: new Date(),
        },
      });

      const chunks: Buffer[] = [];
      doc.on("data", (chunk) => chunks.push(chunk as Buffer));
      doc.on("end", () => resolve(Buffer.concat(chunks)));
      doc.on("error", reject);

      const title = sanitize(lessonPlan?.title) || "Lesson Plan";
      heading(doc, title, 1);
      paragraph(doc, `Grade: ${sanitize(lessonPlan?.grade)} | Duration: ${sanitize(lessonPlan?.duration)}`, {
        size: 10,
        color: "#1e293b",
      });
      if (sanitize(topic)) paragraph(doc, `Topic: ${sanitize(topic)}`, { size: 10, color: "#475569" });

      if ((lessonPlan?.objectives || []).length) {
        heading(doc, "Learning Objectives", 2);
        for (const [i, objective] of lessonPlan.objectives.entries()) {
          bullet(doc, `${i + 1}. ${objective}`);
        }
      }

      for (const day of lessonPlan?.days || []) {
        heading(doc, `Day ${day.day}: ${sanitize(day.topic)}`, 2);
        heading(doc, lessonPlan?.frameworkLabel || framework.sectionTitle, 3);
        for (const phase of day["4asModel"] || []) {
          heading(
            doc,
            `${sanitize(phase.phase)} - ${sanitize(phase.title)} (${Number(phase.timeMinutes || 0)} min)`,
            3
          );
          labeledParagraph(doc, "Description", phase.description);
          labeledParagraph(doc, "Teacher Role", phase.teacherRole);
          labeledParagraph(doc, "Student Role", phase.studentRole);
          labeledParagraph(doc, "Materials", (phase.materials || []).join(", "));
        }

        if (Object.keys(day.specificActivities || {}).length > 0) {
          renderSpecificActivities(doc, day.specificActivities || {});
        }

        if ((day.assessment || []).length) {
          heading(doc, "Assessment and Rubrics", 2);
          for (const assessment of day.assessment) {
            heading(doc, sanitize(assessment.criteria || "Assessment"), 3);
            labeledParagraph(doc, "Description", assessment.description);
            labeledParagraph(doc, "Excellent", assessment.rubricLevel?.excellent || "");
            labeledParagraph(doc, "Satisfactory", assessment.rubricLevel?.satisfactory || "");
            labeledParagraph(doc, "Needs Improvement", assessment.rubricLevel?.needsImprovement || "");
          }
        }

        if (sanitize(day.differentiation)) {
          heading(doc, "Differentiation", 2);
          paragraph(doc, day.differentiation);
        }
        if (sanitize(day.closure)) {
          heading(doc, "Closure", 2);
          paragraph(doc, day.closure);
        }
      }

      addFooter(doc);
      doc.end();
    } catch (error) {
      reject(error);
    }
  });
}

export async function generateLessonPlanPDF(
  lessonPlan: LessonPlanData,
  topic: string
): Promise<Buffer> {
  const preferHtml = process.env.LESSON_PLAN_PDF_ENGINE !== "pdfkit";
  if (preferHtml) {
    try {
      return await generateLessonPlanPDFWithHtml(lessonPlan, topic);
    } catch (error) {
      console.warn("[lesson-plan-pdf] html export failed, falling back to PDFKit:", error);
    }
  }
  return generateLessonPlanPDFWithPdfKit(lessonPlan, topic);
}
